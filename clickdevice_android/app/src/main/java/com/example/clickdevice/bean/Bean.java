package com.example.clickdevice.bean;

public class Bean {
    private int x;
    private int y;

    public void setX(int x) {
        this.x = x;
    }

    public Bean(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString() {
        return "[" +
                "x=" + x +
                ", y=" + y +
                ']';
    }
}
